	CONSTRAINT alunos_fk_curso_id FOREIGN KEY (curso_id) REFERENCES cursos (curso_id),
	CONSTRAINT alunos_fk_turma_id FOREIGN KEY (turma_id) REFERENCES turma (turma_id)
